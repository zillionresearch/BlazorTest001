class I18n {
    constructor() {
        this.translations = translations;
        this.currentLang = this.getStoredLanguage() || this.getBrowserLanguage();
        this.updateUI();
    }

    getStoredLanguage() {
        return localStorage.getItem('moneyworkdesk_lang');
    }

    getBrowserLanguage() {
        const browserLang = navigator.language || navigator.userLanguage;
        const langCode = browserLang.toLowerCase();

        // Check for exact match first (e.g., nl-be)
        if (this.translations[langCode]) {
            return langCode;
        }

        // Check for base language (e.g., nl from nl-nl)
        const baseLang = langCode.split('-')[0];
        if (this.translations[baseLang]) {
            return baseLang;
        }

        // Default to English
        return 'en';
    }

    setLanguage(lang) {
        if (!this.translations[lang]) {
            console.warn(`Language ${lang} not found, using English`);
            lang = 'en';
        }

        this.currentLang = lang;
        localStorage.setItem('moneyworkdesk_lang', lang);
        document.body.setAttribute('data-lang', lang);
        this.updateUI();

        console.log('[I18n] Language changed to:', lang);
    }

    t(key) {
        const keys = key.split('.');
        let value = this.translations[this.currentLang];

        for (const k of keys) {
            if (value && typeof value === 'object') {
                value = value[k];
            } else {
                // Fallback to English
                value = this.translations.en;
                for (const fallbackKey of keys) {
                    if (value && typeof value === 'object') {
                        value = value[fallbackKey];
                    }
                }
                break;
            }
        }

        return value || key;
    }

    updateUI() {
        // Update all elements with data-i18n attribute
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = this.t(key);

            // Handle HTML content (for titles with gradient-text spans)
            if (translation && translation.includes('<')) {
                element.innerHTML = translation;
            } else {
                element.textContent = translation;
            }
        });

        console.log('[I18n] UI updated to', this.currentLang);
    }
}

// Initialize
const i18n = new I18n();
window.i18n = i18n;
